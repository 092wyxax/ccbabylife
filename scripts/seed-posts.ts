async function main() {
  const url = process.env.DATABASE_URL
  if (!url) throw new Error('DATABASE_URL not set')

  const client = postgres(url, { prepare: false })
  const db = drizzle(client)

  console.log('Seeding journal posts...')

  for (const p of SEED_POSTS) {
    const existing = await db
      .select()
      .from(posts)
      .where(and(eq(posts.orgId, DEFAULT_ORG_ID), eq(posts.slug, p.slug)))
      .limit(1)

    if (existing[0]) {
      await db
        .update(posts)
        .set({
          title: p.title,
          excerpt: p.excerpt,
          body: p.body,
          heroImage: p.heroImage ?? existing[0].heroImage,
          status: 'active',
          publishedAt: existing[0].publishedAt ?? new Date(),
          updatedAt: new Date(),
        })
        .where(eq(posts.id, existing[0].id))
      console.log(`  updated: ${p.slug}`)
    } else {
      await db.insert(posts).values({
        orgId: DEFAULT_ORG_ID,
        slug: p.slug,
        title: p.title,
        excerpt: p.excerpt,
        body: p.body,
        heroImage: p.heroImage,
        status: 'active',
        publishedAt: new Date(),
      })
      console.log(`  inserted: ${p.slug}`)
    }
  }

  console.log(`Done. ${SEED_POSTS.length} posts seeded.`)
  await client.end()
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
